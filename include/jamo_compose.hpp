#pragma once
#include <string>

namespace braille
{

// 호환 자모(U+3131~U+3163)로 분리된 문자열에서 초성+중성(+종성) 시퀀스를
// 찾아 완성형 한글 음절(U+AC00~U+D7A3)로 합친다.
//
// 원본 파이썬 코드는 외부 라이브러리의 join_jamos() 함수를 그대로 가져다 썼고
// 그 구현은 제공된 소스에 포함되어 있지 않다. 아래는 동일한 결과를 내도록
// 표준 한글 결합 규칙(NFC 합성 규칙과 동일)으로 직접 구현한 것이다:
//   - 초성 다음에 중성이 오면 음절을 시작한다.
//   - 그 다음 글자가 종성이 될 수 있는 자음이면 종성 후보로 본다.
//   - 단, 종성 후보 자음이 "초성으로도 쓰일 수 있는 자음"이고 그 바로 다음
//     글자가 다시 중성(모음)이면, 그 자음은 다음 음절의 초성으로 양보한다.
//     (예: "ㄱㅏㄴㅏ" -> "가나", "간"+낱자 "ㅏ" 가 되지 않도록)
//   - 결합 대상이 아닌 문자는 그대로 통과시킨다.
std::u32string join_jamos(const std::u32string &s);

} // namespace braille
